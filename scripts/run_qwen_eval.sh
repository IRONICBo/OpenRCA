#!/bin/bash
# End-to-end evaluation with Qwen via vLLM
# Prerequisites: vLLM server must be running (see scripts/start_vllm.sh)
#
# Usage:
#   bash scripts/run_qwen_eval.sh                                    # default: agent on all datasets
#   bash scripts/run_qwen_eval.sh --dataset Bank                    # single dataset
#   bash scripts/run_qwen_eval.sh --method balanced --mode cot      # sampling baseline
#   VLLM_HOST=10.0.0.1 VLLM_PORT=8000 bash scripts/run_qwen_eval.sh  # remote vLLM
set -e

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
PROJECT_DIR="$(dirname "$SCRIPT_DIR")"
cd "$PROJECT_DIR"

if [ -f "venv/bin/activate" ]; then
    source venv/bin/activate
fi

# vLLM server connection
VLLM_HOST="${VLLM_HOST:-localhost}"
VLLM_PORT="${VLLM_PORT:-8000}"
VLLM_URL="http://${VLLM_HOST}:${VLLM_PORT}/v1"

# Defaults
METHOD="${METHOD:-agent}"
MODE="${MODE:-direct}"
DATASET="${DATASET:-}"
TAG="${TAG:-qwen}"
NO_WANDB=""
WANDB_PROJECT="${WANDB_PROJECT:-OpenRCA}"

EXTRA_ARGS=""
while [[ $# -gt 0 ]]; do
    case $1 in
        --method) METHOD="$2"; shift 2 ;;
        --mode) MODE="$2"; shift 2 ;;
        --dataset) DATASET="$2"; shift 2 ;;
        --tag) TAG="$2"; shift 2 ;;
        --no_wandb) NO_WANDB="--no_wandb"; shift ;;
        --wandb_project) WANDB_PROJECT="$2"; shift 2 ;;
        --vllm_host) VLLM_HOST="$2"; VLLM_URL="http://${VLLM_HOST}:${VLLM_PORT}/v1"; shift 2 ;;
        --vllm_port) VLLM_PORT="$2"; VLLM_URL="http://${VLLM_HOST}:${VLLM_PORT}/v1"; shift 2 ;;
        *) EXTRA_ARGS="$EXTRA_ARGS $1"; shift ;;
    esac
done

# 1. Check vLLM server is reachable
echo "Checking vLLM server at ${VLLM_URL} ..."
if ! curl -s --max-time 5 "${VLLM_URL}/models" > /dev/null 2>&1; then
    echo "ERROR: Cannot reach vLLM server at ${VLLM_URL}"
    echo "  Start it with: bash scripts/start_vllm.sh"
    echo "  Or set VLLM_HOST/VLLM_PORT for a remote server."
    exit 1
fi

# Get the served model name from vLLM
SERVED_MODEL=$(curl -s "${VLLM_URL}/models" | python -c "import sys,json; print(json.load(sys.stdin)['data'][0]['id'])" 2>/dev/null || echo "unknown")
echo "vLLM serving model: ${SERVED_MODEL}"

# 2. Update api_config.yaml to point to vLLM
cat > rca/api_config.yaml <<EOF
# Auto-generated by run_qwen_eval.sh
SOURCE:     "vLLM"
MODEL:      "${SERVED_MODEL}"
API_KEY:    "EMPTY"
API_BASE:   "${VLLM_URL}"
MAX_TOKENS: 8192
MAX_RETRIES: 5
EOF

echo ""
echo "============================================"
echo "  OpenRCA Qwen/vLLM Evaluation"
echo "============================================"
echo "  Model:    ${SERVED_MODEL}"
echo "  Server:   ${VLLM_URL}"
echo "  Method:   ${METHOD}"
echo "  Mode:     ${MODE}"
echo "  Dataset:  ${DATASET:-all}"
echo "  Tag:      ${TAG}"
echo "  Wandb:    ${NO_WANDB:-enabled}"
echo "============================================"
echo ""

DATASETS=("Market/cloudbed-1" "Market/cloudbed-2" "Bank" "Telecom")

if [ "$METHOD" == "agent" ]; then
    if [ -n "$DATASET" ]; then
        # Single dataset
        bash scripts/run_agent.sh --dataset "$DATASET" --tag "$TAG" \
            --wandb_project "$WANDB_PROJECT" $NO_WANDB $EXTRA_ARGS
    else
        # All datasets
        for ds in "${DATASETS[@]}"; do
            echo "--- RCA-Agent on $ds ---"
            bash scripts/run_agent.sh --dataset "$ds" --tag "$TAG" \
                --wandb_project "$WANDB_PROJECT" $NO_WANDB $EXTRA_ARGS || {
                echo "WARNING: Agent on $ds failed, continuing..."
            }
        done
    fi
elif [ "$METHOD" == "balanced" ] || [ "$METHOD" == "oracle" ]; then
    if [ -n "$DATASET" ]; then
        bash scripts/run_sampling.sh --method "$METHOD" --mode "$MODE" \
            --dataset "$DATASET" --tag "$TAG" \
            --wandb_project "$WANDB_PROJECT" $NO_WANDB $EXTRA_ARGS
    else
        for ds in "${DATASETS[@]}"; do
            echo "--- ${METHOD} ${MODE} on $ds ---"
            bash scripts/run_sampling.sh --method "$METHOD" --mode "$MODE" \
                --dataset "$ds" --tag "$TAG" \
                --wandb_project "$WANDB_PROJECT" $NO_WANDB $EXTRA_ARGS || {
                echo "WARNING: ${METHOD} ${MODE} on $ds failed, continuing..."
            }
        done
    fi
else
    echo "ERROR: Unknown method '$METHOD'. Use 'agent', 'balanced', or 'oracle'."
    exit 1
fi

echo ""
echo "============================================"
echo "  Evaluation complete!"
echo "  Results:  test/result/"
echo "  Wandb:    https://wandb.ai"
echo "============================================"
